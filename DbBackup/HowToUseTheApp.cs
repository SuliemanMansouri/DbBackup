            InitializeComponent();
            HowToTextBox.Text = @"تعليمات إعداد واستخدام برنامج النسخ الاحتياطي:

1. إعداد ملف الإعدادات:
- عند تشغيل البرنامج لأول مرة، سيتم إنشاء ملف باسم backupconfig.json في مجلد البرنامج.
- افتح الملف باستخدام المفكرة أو أي محرر نصوص.
- أدخل معلومات قاعدة البيانات:
  - Server: اسم أو عنوان الخادم.
  - Database: اسم قاعدة البيانات.
  - Destinations: قائمة بمسارات المجلدات التي سيتم حفظ النسخ الاحتياطية فيها.
  - SaveToRemovable: إذا كنت تريد نسخ النسخة الاحتياطية إلى وحدة تخزين خارجية (فلاش)، اجعل القيمة true.
  - UseSchedule: لتفعيل الجدولة التلقائية، اجعل القيمة true.
  - ScheduledTimes: قائمة بالأوقات (بتنسيق HH:mm مثل ""02:00"") التي سيتم فيها تنفيذ النسخ الاحتياطي تلقائياً.

2. تشغيل البرنامج:
- عند تشغيل البرنامج، ستظهر نافذة صغيرة ويمكن تصغيرها إلى شريط النظام.
- يمكنك الضغط على زر ""نسخ احتياطي"" لتنفيذ النسخ الاحتياطي يدوياً في أي وقت.

3. تفعيل النسخ المجدول:
- لتفعيل النسخ الاحتياطي المجدول، فعّل خيار ""استخدم الجدول الزمني"".
- سيتم فتح ملف الإعدادات تلقائياً لتعديل أوقات الجدولة.
- أضف أو عدّل الأوقات حسب رغبتك (مثال: ""02:00"", ""14:00"").
- احفظ الملف وأعد تشغيل البرنامج إذا لزم الأمر.

4. استعادة النسخة الاحتياطية:
- ستجد الملفات المضغوطة للنسخ الاحتياطية في المجلدات التي حددتها في Destinations.
- يمكنك فك الضغط عن الملف واستعادة قاعدة البيانات باستخدام SQL Server Management Studio أو أي أداة مناسبة.

ملاحظات:
- تأكد من أن البرنامج يعمل بصلاحيات كافية للوصول إلى مجلدات الوجهة.
- يجب أن يكون لديك صلاحيات النسخ الاحتياطي على قاعدة البيانات.
- في حال ظهور أي رسالة خطأ، تحقق من ملف الإعدادات أو اتصل بالدعم الفني.
";
